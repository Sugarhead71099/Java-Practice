public class Resolution {

    // Instance Fields
    private int width;
    private int height;

    // Default Constructor
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    // Getters
    public int getWidth() {
        return width;
    }
    public int getHeight() {
        return height;
    }

}
